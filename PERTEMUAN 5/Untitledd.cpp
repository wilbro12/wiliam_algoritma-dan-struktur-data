#include <iostream>
using namespace std;

int main()
{
    int a;
    
    cout << "Contoh Program perulangan" << endl;
    for (int a = 1; a <= 10; a++) {
        cout << a << endl;
    }

    cout << "\nPerulangan menurun" << endl;
    for (a = 10; a >= 1; a--) {
        cout << a << endl;
    }

    return 0;
}
